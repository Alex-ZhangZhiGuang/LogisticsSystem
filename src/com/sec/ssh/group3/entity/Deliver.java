	/** minimal constructor */
	public Deliver(int did, User user, Orders orders, Date deliverydate,
			int issendsigninfo) {
		this.did = did;
		this.user = user;
		this.orders = orders;
		this.deliverydate = deliverydate;
		this.issendsigninfo = issendsigninfo;
	}

